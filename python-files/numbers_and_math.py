#       Symbol         |        Name
#---------------------------------------
#          +           |       Addition
#          -           |       Substraction 
#          *           |       Multiplication
#          /           |       Division 
#          **          |       Exponentiation
#          %           |       Modulo (reminder operator)
#          //          |       Integer Division
# There are two number types in python: Integer and Float
print (1/2) #division will always return a float
print ((1+2)*5/3) #division will always return a float
print (1+2*5/3) #division will always return a float except for Integer Division with // operator
print (2**3)
print (10%3)
print (10//3)
print (6.5*2)
print (6/2)
